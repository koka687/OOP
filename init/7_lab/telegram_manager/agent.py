team_post_creation_agent = ParallelAgent(
    name="team_post_creation",
    sub_agents=[gemini_editor, flash_editor],
    description="Паралельне створення двох версій медіаплану для Telegram: одна для загальної аудиторії, інша — для студентів ІТ.",
)

team_post_edition_agent = LoopAgent(
    name="team_post_edition",
    sub_agents=[create_post, critisism],
    description="Цикл редагування посту на основі критики. Створює пост, отримує критику, і якщо є зауваження — редагує пост знову, поки не отримає 'Готовий до публікації.'",
    max_iterations=2
)

