# Простий калькулятор у терміналі
a = float(input("Введіть перше число: "))
b = float(input("Введіть друге число: "))
op = input("Оберіть операцію (+, -, *, /): ")

if op == "+":
    print("Результат:", a + b)
elif op == "-":
    print("Результат:", a - b)
elif op == "*":
    print("Результат:", a * b)
elif op == "/":
    if b != 0:
        print("Результат:", a / b)
    else:
        print("Ділення на нуль!")
else:
    print("Невідома операція")